class SimplifiedEnum(type):
    def __new__(mcs, cls_name, bases, cls_dict):
        cls_instance = super().__new__(mcs, cls_name, bases, cls_dict)
        for attr in cls_dict[f"_{cls_name}__keys"]:
            if not hasattr(cls_instance, attr):
                setattr(cls_instance, attr, attr)
        return cls_instance
